class Bulglish {
	
	public static function slugify($text) {
		
		$expressions = [
			'/[иИ][яЯ]/u' => 'ia',
			'/[аА]/u' => 'a',
			'/[бБ]/u' => 'b',
			'/[вВ]/u' => 'v',
			'/[гГ]/u' => 'g',
			'/[дД]/u' => 'd',
			'/[ѐеЕ]/u' => 'e',
			'/[ёЁ]/u' => 'ye',
