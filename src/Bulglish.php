			'/[иИ]/u' => 'i',
			'/[ѝЍ]/u' => 'i',
			'/[йЙ]/u' => 'y',
			'/[кК]/u' => 'k',
			'/[лЛ]/u' => 'l',
			'/[мМ]/u' => 'm',
			'/[нН]/u' => 'n',
			'/[оО]/u' => 'o',
			'/[пП]/u' => 'p',
			'/[рР]/u' => 'r',
			'/[сС]/u' => 's',
			'/[тТ]/u' => 't',
			'/[уУ]/u' => 'u',
			'/[фФ]/u' => 'f',
			'/[хХ]/u' => 'h',
			'/[цЦ]/u' => 'ts',
			'/[чЧ]/u' => 'ch',
			'/[шШ]/u' => 'sh',
			'/[щЩ]/u' => 'sht',
			'/[ъЪ]/u' => 'a',
			'/[ыЫ]/u' => 'y',
			'/[ь]/u' => 'y',
			'/[эЭ]/u' => 'e',
			'/[юЮ]/u' => 'yu',
			'/[яЯ]/u' => 'ya',
			'/[^a-zA-Z01-9]/i' => '-',
			'/(-+)/i' => '-',
			'/(-$|^-)/i' => '',
		];
		
		$text = preg_replace( array_keys($expressions), array_values($expressions), $text );
		return $text;
		
	}
	
